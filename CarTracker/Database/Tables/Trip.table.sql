CREATE TABLE TRIP (
	ID BIGINT NOT NULL AUTO_INCREMENT,
	START_DATE DATETIME NOT NULL,
	END_DATE DATETIME NULL,
	NAME VARCHAR(250) NULL,
	CAR_ID BIGINT NOT NULL,
	AVERAGE_SPEED DOUBLE NULL,
	MAX_SPEED DOUBLE NULL,
	AVERAGE_ENGINE_RPM DOUBLE NULL,
	MAX_ENGINE_RPM DOUBLE NULL,
	DISTANCE_TRAVELED DOUBLE NULL,
	IDLE_TIME BIGINT NULL,
	START_PLACE BIGINT NULL,
	DESTINATION_PLACE BIGINT NULL,
	STATUS INT NOT NULL,
	CREATE_DATE DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
	MODIFIED_DATE DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
	CONSTRAINT PK_TRIP PRIMARY KEY (ID),
	CONSTRAINT FK_TRIP_CAR FOREIGN KEY (CAR_ID) REFERENCES CAR(ID),
	CONSTRAINT FK_TRIP_STATUS FOREIGN KEY (STATUS) REFERENCES TRIP_STATUS(ID),
	CONSTRAINT FK_TRIP_PLACE_START FOREIGN KEY (START_PLACE) REFERENCES PLACE(ID),
	CONSTRAINT FK_TRIP_PLACE_DESTINATION FOREIGN KEY (DESTINATION_PLACE) REFERENCES PLACE(ID)
);
